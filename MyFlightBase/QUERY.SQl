SELECT * FROM COUNTRY WHERE CountryID NOT IN (SELECT DISTINCT CountryID FROM CITY);
SELECT * FROM CITY WHERE CityID IN (SELECT DepartureCityID FROM FLIGHT WHERE DistanceKm > 1000);
SELECT * FROM PLANE_TYPE WHERE Capacity = (SELECT MAX(Capacity) FROM PLANE_TYPE);

