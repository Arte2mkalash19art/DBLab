SELECT F.FlightNumber, Ci.CityName AS DepartureCity, P.ModelName FROM FLIGHT F JOIN CITY Ci ON F.DepartureCityID = Ci.CityID JOIN PLANE_TYPE P ON F.PlaneTypeID = P.PlaneTypeID;
SELECT Co.CountryName, Ci.CityName FROM COUNTRY Co LEFT JOIN CITY Ci ON Co.CountryID = Ci.CountryID; -- Всі країни, навіть без міст
SELECT P.ModelName, F.FlightNumber FROM PLANE_TYPE P RIGHT JOIN FLIGHT F ON P.PlaneTypeID = F.PlaneTypeID;
SELECT * FROM FLIGHT F JOIN CITY C1 ON F.DepartureCityID = C1.CityID JOIN CITY C2 ON F.ArrivalCityID = C2.CityID;

