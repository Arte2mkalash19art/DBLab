USE MyFlightBase;
GO

IF OBJECT_ID('FLIGHT', 'U') IS NOT NULL DROP TABLE FLIGHT;
IF OBJECT_ID('CITY', 'U') IS NOT NULL DROP TABLE CITY;
IF OBJECT_ID('PLANE_TYPE', 'U') IS NOT NULL DROP TABLE PLANE_TYPE;
IF OBJECT_ID('COUNTRY', 'U') IS NOT NULL DROP TABLE COUNTRY;
GO

CREATE TABLE COUNTRY (
    CountryID INT IDENTITY(1,1) PRIMARY KEY,
    CountryName NVARCHAR(100) NOT NULL UNIQUE,
    RequiresCustoms BIT NOT NULL,
    IsInUNList BIT NOT NULL
);

CREATE TABLE CITY (
    CityID INT IDENTITY(1,1) PRIMARY KEY,
    CountryID INT NOT NULL,
    CityName NVARCHAR(100) NOT NULL UNIQUE,
    CONSTRAINT FK_CITY_COUNTRY FOREIGN KEY (CountryID) REFERENCES COUNTRY(CountryID)
        ON DELETE CASCADE 
        ON UPDATE CASCADE
);

CREATE TABLE PLANE_TYPE (
    PlaneTypeID INT IDENTITY(1,1) PRIMARY KEY,
    ModelName NVARCHAR(100) NOT NULL UNIQUE,
    Capacity SMALLINT NOT NULL CHECK (Capacity > 0)
);

CREATE TABLE FLIGHT (
    FlightID INT IDENTITY(1,1) PRIMARY KEY,
    FlightNumber VARCHAR(10) NOT NULL UNIQUE,
    DepartureCityID INT NOT NULL,
    ArrivalCityID INT NOT NULL,
    PlaneTypeID INT NOT NULL,
    DepartureDateTime DATETIME2 NOT NULL,
    DistanceKm INT NOT NULL CHECK (DistanceKm > 0),
    CONSTRAINT FK_FLIGHT_DEPARTURE FOREIGN KEY (DepartureCityID) REFERENCES CITY(CityID)
        ON DELETE NO ACTION ON UPDATE NO ACTION,
    CONSTRAINT FK_FLIGHT_ARRIVAL FOREIGN KEY (ArrivalCityID) REFERENCES CITY(CityID)
        ON DELETE NO ACTION ON UPDATE NO ACTION,
    CONSTRAINT FK_FLIGHT_PLANE FOREIGN KEY (PlaneTypeID) REFERENCES PLANE_TYPE(PlaneTypeID)
        ON DELETE CASCADE 
        ON UPDATE CASCADE,
    CHECK (DepartureCityID <> ArrivalCityID)
);
GO